// sederhananya 
// while > bertanya dulu baru jalan
// do while > jalan dulu baru bertanya


let ngulangUjian = 0;

console.log("\nIni program While")
while (ngulangUjian < 3){
    ngulangUjian += 1;
    console.log("Pengulangan ", ngulangUjian);
}


let ngulangUjian2 = 0;
console.log("\nIni program do While")
do {
    ngulangUjian2 += 1;
    console.log("pengulangan ", ngulangUjian2);
} while (ngulangUjian2 < 3);